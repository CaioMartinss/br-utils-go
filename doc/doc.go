/*
Pacote br_utils_go fornece utilitários para validação e formatação de dados brasileiros, como CPF, CNPJ, CEP e números de telefone.

O objetivo deste projeto é oferecer um conjunto abrangente de ferramentas para lidar com tarefas comuns de validação e formatação de dados brasileiros, simplificando o processo de desenvolvimento para aplicações que requerem tais funcionalidades.

# Funcionalidades

- Validação de CPF (Cadastro de Pessoas Físicas)
- Formatação de números de CPF
- Validação de CNPJ (Cadastro Nacional da Pessoa Jurídica)
- Formatação de números de CNPJ
- Validação e formatação de números de telefone brasileiros
- Consulta de informações de CEP (Código de Endereçamento Postal) via APIs externas

# Instalação

Para instalar o pacote, execute:

	go get github.com/CaioMartinss/br-utils-go

# Uso

Importe o pacote no seu projeto Go:

	import "github.com/CaioMartinss/br-utils-go/validation"
	import "github.com/CaioMartinss/br-utils-go/external"
	import "github.com/CaioMartinss/br-utils-go/examples"
	
# Estrutura do Projeto

- validation: Contém funções para validação e formatação de CPF, CNPJ e números de telefone.
- external: Contém funções para consulta de informações de CEP utilizando APIs externas.
- examples: Contém exemplos de uso das funções do pacote.

*/

package br_utils_go
